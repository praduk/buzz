/*********************Format of a Recognizer Signature*************************
Bit     (1)	0       indicates the existance of white pawns
Bit     (1)	1       indicates the existance of white knights
Bit     (1)	2       indicates the existance of white bishops
Bit     (1)	3       indicates the existance of white rooks
Bit     (1)	4       indicates the existance of white queens
Bit     (1)	5       indicates the existance of black pawns
Bit     (1)	6       indicates the existance of black knights
Bit     (1)	7       indicates the existance of black bishops
Bit     (1)	8       indicates the existance of black rooks
Bit     (1)	9       indicates the existance of black queens
*******************************************************************************/

//Recognizer Signature
#define rsig(msigWHITE,msigBLACK) (((msigBLACK)<<5)|(msigWHITE))

#define extractMatSig(recogsig,side) (side?(recogsig)>>5:(recogsig)&0x1F)

//Material Signature
#define msig(p,n,b,r,q) ( (p?1:0)|((n?1:0)<<1)|((b?1:0)<<2)|((r?1:0)<<3)|((q?1:0)<<4) )
#define msigHasPiece(matsig,piece) ((matsig)&(1<<(piece)))


#define genrsig(pos) rsig(\
	msig(piecesWHITE(pos,P),piecesWHITE(pos,N),piecesWHITE(pos,B),piecesWHITE(pos,R),piecesWHITE(pos,Q))  ,\
	msig(piecesBLACK(pos,P),piecesBLACK(pos,N),piecesBLACK(pos,B),piecesBLACK(pos,R),piecesBLACK(pos,Q))  )

#define updateRecogSig(sig,side,piece,existance) \
{ \
	if(existance) \
		(sig)|=(1<<(piece))<<(5*(side)); \
	else \
		(sig)&=~((1<<(piece))<<(5*(side))); \
}

